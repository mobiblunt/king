    <h1 class="fs-48 fw-200 tac">Commercial Escrows</h1>
  </div>
</div>
<div class="bc-white py-40">
  <div class="row-90 bb-light-grey">
    <div class="content-1 mb-40">
      <p class="fs-18 wdt-80 ctr tac">The term commercial escrow can cover a wide variety of transactions, and <strong>Escrow Custodian Services</strong> have a team dedicated to providing payment gateway solutions for most of them.</p>
<p class="fs-18 wdt-80 ctr tac">Â </p>
<p class="fs-18 wdt-80 ctr tac">In essence, of course, any transaction between two businesses who are not used to working together and who seek a neutral third party to help ensure there are no financial issues, could be regarded as a commercial escrow.</p>
    </div>
  </div>
</div>
<div class="bc-white">
  <div class="row-90 pb-40">
    <div class="column-11-24">
      <div class="content-1">
        <p class="fs-52 fw-200"><span class="fc-terquoise">Project Finance</span></p>
      </div>
    </div>
    <div class="column-2-24 empty">
      <!-- Nothing to see here! -->
    </div>
    <div class="column-11-24">
      <div class="content-1">
        <p>A common form of commercial escrow may be where company A has won a contract, and company B who awarded the contract is now expected to make a large first stage payment up front. That payment may be placed in a commercial escrow until the stage is completed, and the conditions are regarded as having been met, and so on.</p>
<p>&nbsp;</p>
<p>Until such stage as the terms have all been completed, the money is not released by the escrow company, which could take some time and may require multiple disbursements.</p>
      </div>
    </div>
  </div>
</div>
<div class="row-90 fc-white py-40">
  <div class="column-11-24">
    <div class="content-1">
      <p class="fs-36 fw-200"><span class="fc-light-grey">Financial services</span></p>
<p>&nbsp;</p>
<p>There are any number of commercial escrows that may fall under the umbrella term of commercial escrow and which may include:</p>
<p>&nbsp;</p>
<ul>
<li><em>Advance fee payments for broker arranged loans</em></li>
<li><em>Company share sales</em></li>
<li><em>The exchange of financial instruments</em></li>
<li><em>Earn out provisions</em></li>
<li><em>Golden handcuff provisions</em></li>
<li><em>Purchase price provisions</em></li>
<li><em>Purchase price adjustment</em></li>
<li><em>Settlement of funds arising from liquidations</em></li>
</ul>
    </div>
  </div>
  <div class="column-2-24 py-40 empty">
    <div class="hr-light-grey"></div>
  </div>
  <div class="column-11-24">
    <div class="content-1">
      <p class="fs-36 fw-200"><span class="fc-light-grey">Mergers and acquisitions escrows</span></p>
<p>&nbsp;</p>
<p>Escrows can help to mitigate risk for many transactions that are part of mergers and acquisitions on behalf of both buyer and seller.</p>
<p>&nbsp;</p>
<p>Holdback escrows are commonly used where an agreed percentage of the agreed purchase price is retained within an escrow.</p>
<p>&nbsp;</p>
<p>Escrows may be tailored to cover most reasonable eventualities.</p>
